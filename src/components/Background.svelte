  <p>
    Volgens de <a href="https://www.anwb.nl/belangenbehartiging/duurzaam/milieuzones">ANWB</a>, zijn er vanaf 2020 nieuwe regels actief met betrekking tot milieuzones. 
    Het doel van milieuzones is helder: minder vervuiling van de lucht in drukke gebieden..
  </p>
  <ul>
    <li>Momenteel kunnen in milieuzones alleen voertuigen geweigerd worden die oude dieselmotoren hebben.</li>
    <li>Tussen 2020 en 2025, kunnen gemeentes er voor kiezen om meer type voertuigen te weigeren: elk voertuig beneden emissieklasse 3 or of 4</li>
    <li>Vanaf 2025 kunnen gemeentes nog meer voertuigen weigeren: elk voertuig beneden emissieklasse 4 of 5</li>
  </ul>
  <p>
    <a href="https://www.carnext.com/nl-be/euronorm/#typeeurnormen">Emissieklassen / euronorm uitleg</a>
  </p>
  <p>
    In totaal zijn er 6 emissieklasses. Dit betekent dat vanaf 2025 de gemeentes kunnen bepalen dat 66% van de emissieklasses niet meer in hun milieuzone mogen komen. 
    Deze ontwikkelingen gaan erg snel, dus wat betekent dit voor de toekomst van de milieuzones?
  </p>
  <p>
    Het logische doel van de milieuzones is dat het automobilisten pusht om minder kilometers te rijden of om over te stappen op milieuvriendelijker transport, zoals de elektrische auto.
    Maar bieden gemeentes met een milieuzone ook betere faciliteiten voor dit gedrag in milieuzones?
  </p>
